package com.niuchaoqun.example;

public class ResponseData {
	private String s;
	private int i;
	private boolean b;
	private String n;

	public String getS() {
		return s;
	}

	public void setS(String s) {
		this.s = s;
	}

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public boolean isB() {
		return b;
	}

	public void setB(boolean b) {
		this.b = b;
	}

	public String getN() {
		return n;
	}

	public void setN(String n) {
		this.n = n;
	}
}